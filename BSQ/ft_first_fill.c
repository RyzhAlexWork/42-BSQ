#include "t_map.h"

void	ft_first_fill(int **new_array, t_map *map)
{
	int	i;
	int j;

	i = 0;
	j = 0;
	while (j < map->l)
	{
		while (i < map->w)
		{
			new_array[j][i] = 1;
			i++;
		}
		i = 0;
		j++;
	}
}
